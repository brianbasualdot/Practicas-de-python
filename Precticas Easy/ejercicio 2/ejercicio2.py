# Suponga que un individuo desea invertir su capital en un banco y
# desea saber cuanto dinero ganara despues de un mes si el banco paga 
# una tasa del 9% anual.

capital = float(input("Escribe el capital a invertir: "))
ganancias = capital * .09

print("La ganancia obtenida por el capital invertido es: $",ganancias)
print("La ganancia total con el capital invertido es: ",capital + ganancias)